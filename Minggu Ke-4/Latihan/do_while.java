public class do_while {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
           // untuk membuat variabel
        int i = 0;
        
        do {
            System.out.println("perulangan ke-" + i);
            i++;
        } while ( i <= 10);
    }
    
}
